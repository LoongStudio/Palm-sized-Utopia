// 基础资源类型
public enum ResourceType
{
    Seed,      // 种子
    Crop,      // 作物
    Feed,      // 饲料
    Livestock, // 种畜
    Animal,    // 牲畜
    Coin,      // 金币
    Ticket     // 奖励券
}

// 种子子类型
public enum SeedSubType
{
    Wheat,     // 小麦种子
    Corn       // 玉米种子
}

// 作物子类型
public enum CropSubType
{
    Wheat,     // 小麦
    Corn       // 玉米
}

// 种畜子类型
public enum LivestockSubType
{
    Cattle,    // 种牛
    Sheep      // 种羊
}

// 牲畜子类型
public enum AnimalSubType
{
    Cattle,    // 牛
    Sheep      // 羊
} 